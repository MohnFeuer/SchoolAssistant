package com.example.imoocmusicdemo.entities;

public class Speclass {

    private Integer speclassId;

    private String speclassName;

    private Integer speclassTime;

    private String speclassLoc;

    private Integer teacherId;

    public Integer getSpeclassId() {
        return speclassId;
    }

    public void setSpeclassId(Integer speclassId) {
        this.speclassId = speclassId;
    }

    public String getSpeclassName() {
        return speclassName;
    }

    public void setSpeclassName(String speclassName) {
        this.speclassName = speclassName;
    }

    public Integer getSpeclassTime() {
        return speclassTime;
    }

    public void setSpeclassTime(Integer speclassTime) {
        this.speclassTime = speclassTime;
    }

    public String getSpeclassLoc() {
        return speclassLoc;
    }

    public void setSpeclassLoc(String speclassLoc) {
        this.speclassLoc = speclassLoc;
    }

    public Integer getTeacherId() {
        return teacherId;
    }

    public void setTeacherId(Integer teacherId) {
        this.teacherId = teacherId;
    }
}
